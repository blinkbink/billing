package id.co.keriss.consolidate.util.ee;

public class Data {
	
	String field;
	String value;
		
	public Data(String field, String value) {
		super();
		this.field = field;
		this.value = value;
	}
	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
